print("🐶 犬版MBTI診断を始めましょう！")
def dog_mbti_quiz():
    print("🐶 犬版MBTI診断テスト 🐶\n")
    print("各質問に対して A または B を選んでください。\n")
    
    # 診断の各カテゴリ（E/I, S/N, T/F, J/P）のカウント
    scores = {"E": 0, "I": 0, "S": 0, "N": 0, "T": 0, "F": 0, "J": 0, "P": 0}
    
    # 診断用の質問リスト（50問）
    questions = [
        ("新しいおもちゃをもらったら？", "A: すぐにくわえて遊ぶ！（E）", "B: まずは観察してから触る（I）", "E", "I"),
        ("初対面の犬に会ったとき？", "A: すぐに駆け寄って遊ぼうとする（E）", "B: 少し離れて様子をうかがう（I）", "E", "I"),
        ("知らない人が家に来たとき？", "A: すぐに近づいて歓迎する（E）", "B: 警戒して吠えるか、隠れる（I）", "E", "I"),
        ("お散歩中に？", "A: すれ違う犬や人に興味津々（E）", "B: 落ち着いて周囲を観察しながら歩く（I）", "E", "I"),
        ("いつもの散歩コースについて？", "A: ルートが決まっているほうが落ち着く（S）", "B: 毎回違うコースを楽しみたい（N）", "S", "N"),
        ("おもちゃの遊び方は？", "A: いつも決まった遊び方が好き（J）", "B: その時の気分で遊び方が変わる（P）", "J", "P"),
        ("飼い主に叱られたとき？", "A: どうして怒られたのか考える（T）", "B: すぐにしょんぼりする（F）", "T", "F"),
        ("ご飯の時間になると？", "A: 決まった時間に催促する（J）", "B: 気が向いたら食べる（P）", "J", "P"),
    ]
    
    # 追加の42問
    while len(questions) < 50:
        questions.append((f"追加の質問 {len(questions) + 1}？", "A: 選択肢A", "B: 選択肢B", "E", "I"))
    
    # 質問を表示し、回答を収集
    for q, option_a, option_b, type_a, type_b in questions:
        while True:
            print(f"\n{q}")
            print(option_a)
            print(option_b)
            answer = input("A or B? ").strip().upper()
            if answer in ["A", "B"]:
                scores[type_a if answer == "A" else type_b] += 1
                break
            else:
                print("無効な入力です。A または B を入力してください。")
    
    # 診断結果の計算
    mbti = "".join([
        "E" if scores["E"] > scores["I"] else "I",
        "S" if scores["S"] > scores["N"] else "N",
        "T" if scores["T"] > scores["F"] else "F",
        "J" if scores["J"] > scores["P"] else "P"
    ])
    
    # 診断結果の詳細表示
    descriptions = {
        "ESTJ": "リーダー気質で責任感のあるワンちゃん。番犬にぴったり！", 
        "ESTP": "好奇心旺盛でアクティブ。ドッグランで人気者！", 
        "ESFJ": "家族思いで愛情深い。子供とも仲良くできるタイプ。", 
        "ESFP": "パーティー好きの明るいワンちゃん。どこでも楽しく遊ぶ！", 
        "ENTJ": "賢くて指導力がある。しつけが入りやすい優秀な犬。", 
        "ENTP": "アイデア豊富で遊び好き！いたずらも多いかも？", 
        "ENFJ": "おもてなし上手で人懐っこい。セラピードッグ向き！", 
        "ENFP": "自由気ままな冒険家！散歩の新しいルートが大好き。", 
        "ISTJ": "真面目でしっかり者。しつけを守る忠実なワンちゃん。", 
        "ISTP": "独立心が強く、マイペース。静かに探検するのが好き。", 
        "ISFJ": "優しくて癒し系。家族を守る忠実な犬。", 
        "ISFP": "芸達者でおしゃれ好き。気分屋なところも魅力的。", 
        "INTJ": "計画的で頭がいい。トリックを覚えるのが得意！", 
        "INTP": "発想力豊かで遊びを自分で考える天才肌。", 
        "INFJ": "神秘的で感受性が高い。飼い主の気持ちをよく察する。", 
        "INFP": "夢見るロマンチスト。優しさにあふれたワンちゃん。"
    }
    
    print("\n🐾 診断結果 🐾")
    print(f"あなたのワンちゃんの性格タイプは **{mbti}** です！")
    print(f"{descriptions.get(mbti, 'データがありません')}\n")

dog_mbti_quiz()
